export const billingAddressSchema = z.object({
  country: countryCodeSchema,
  state: z.string().min(1, "State is required"),
  city: z.string().min(1, "City is required"),
  street: z.string().min(1, "Street is required"),
  zipcode: z.string().min(1, "Zipcode is required"),
});

export const newCustomerSchema = z.object({
  name: z.string().min(1, "Name is required"),
  email: z.email("Valid email is required"),
  phone_number: z.string().optional(),
});

export const createSubscriptionRequestSchema = z.object({
  product_id: z.string().min(1, "Product ID is required"),
  quantity: z.number().int().min(1, "Quantity must be at least 1"),
  customer: newCustomerSchema,
  billing: billingAddressSchema,
  payment_link: z.boolean().default(true),
  return_url: z.string().url().optional(),
});

export const customerResponseSchema = z.object({
  customer_id: z.string(),
  name: z.string(),
  email: z.string(),
});

export const createSubscriptionResponseSchema = z.object({
  subscription_id: z.string(),
  payment_id: z.string(),
  payment_link: z.string().nullable(),
  recurring_pre_tax_amount: z.number().int(),
  customer: customerResponseSchema,
  metadata: z.record(z.string(), z.string()),
  addons: z.array(z.any()),
  client_secret: z.string().nullable(),
  discount_id: z.string().nullable(),
  expires_on: z.string().datetime().nullable(),
});

export const subscriptionApiResponseSchema = z.object({
  success: z.boolean(),
  error: z.string().optional(),
  data: createSubscriptionResponseSchema.optional(),
});

export const paymentErrorResponseSchema = z.object({
  success: z.boolean().default(false),
  error: z.string(),
});

export type CountryCode = z.infer<typeof countryCodeSchema>;
export type BillingAddress = z.infer<typeof billingAddressSchema>;
export type NewCustomer = z.infer<typeof newCustomerSchema>;
export type CreateSubscriptionRequest = z.infer<
  typeof createSubscriptionRequestSchema
>;
export type CreateSubscriptionResponse = z.infer<
  typeof createSubscriptionResponseSchema
>;
export type CustomerResponse = z.infer<typeof customerResponseSchema>;
export type SubscriptionApiResponse = z.infer<
  typeof subscriptionApiResponseSchema
>;
export type PaymentErrorResponse = z.infer<typeof paymentErrorResponseSchema>;

